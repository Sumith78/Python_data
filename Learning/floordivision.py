# Perform floor division of 7 by 3
floor_division_result = 7 // 3

# Convert 2.7 to an integer
int_converted_value = int(2.7)

# Check if the floor division result is equal to the integer-converted value
is_equal = floor_division_result == int_converted_value

# Print the results
print(f"Floor division of 7 by 3: {floor_division_result}")
print(f"Integer converted value of 2.7: {int_converted_value}")
print(f"Are they equal? {is_equal}")
